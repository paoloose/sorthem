function shuffle(arr) {
  let i = arr.length - 1;
  while (i != 0) {
    const j = Math.floor(Math.random() * (i + 1));
    [arr[i], arr[j]] = [arr[j], arr[i]];
    i--;
  }
  return arr;
}

